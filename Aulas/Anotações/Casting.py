#Casting -- Conversão de tipos:

    #Int 

print('---------------------    Casting do tipo INT   ------------------')
x = int(2)
z = int('2')
y = int(2.0)

print(x, z, y)

print('A variavel x ',x, ' é do tipo:',type(x))
print('A variavel z ',z,' é do tipo:',type(z))
print('A variavel y ' ,y, ' é do tipo:',type(y))

    #float
    
print('---------------------   Casting do tipo Float    ------------------')
