'''8. O proprietário da empresa ABC LTDA precisa de um programa de computador para calcular o novo salário que seus funcionários irão receber a partir do mês que vem. Sabendo que o aumento de salário para todos os funcionários será de 25%, faça um programa que guarda o valor do salário atual do funcionário e informa o seu novo salário acrescido de 25%.'''

